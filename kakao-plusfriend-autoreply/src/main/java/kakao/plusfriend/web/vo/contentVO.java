package kakao.plusfriend.web.vo;

public class contentVO {
	private int txt_id;
	private int reg_id;
	private String txt_content;
	private String reg_content;
	
	
	
	public int getTxt_id() {
		return txt_id;
	}
	public void setTxt_id(int txt_id) {
		this.txt_id = txt_id;
	}
	public int getReg_id() {
		return reg_id;
	}
	public void setReg_id(int reg_id) {
		this.reg_id = reg_id;
	}
	public String getTxt_content() {
		return txt_content;
	}
	public void setTxt_content(String txt_content) {
		this.txt_content = txt_content;
	}
	public String getReg_content() {
		return reg_content;
	}
	public void setReg_content(String reg_content) {
		this.reg_content = reg_content;
	}
	
	
}